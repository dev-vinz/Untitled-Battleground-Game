		public Crocodile()
		{
			Name = NAME;
			Tier = 4;
			Damage = 7;
			Health = 7;
			Ability = Ability.StartOfBattle;
		}

		public override Character Clone()
		{
			return new Crocodile
			{
				Ability = Ability,
				BattlefieldPosition = BattlefieldPosition,
				Damage = Damage,
				Health = Health,
				Level = Level,
				Name = Name,
				Tier = Tier,
			};
		}

		/// <summary>
		/// Deal 7 attack to last oponnent
		/// </summary>
		/// <returns></returns>
		public override AbilityEventArgs TriggerAbility()
		{
			int index = 4;
			return new CrocodileEventArgs(Side.Opponent, index, 7 * Level); // It's to the combat to see if it's a valid position
		}

