namespace Entities.Characters.Tier1
{
	public class Mosquito : Character
	{
		public const string NAME = "Mosquito";

		/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *\
		|*                               FIELDS                              *|
		\* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */



		/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *\
		|*                             PROPERTIES                            *|
		\* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */



		/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *\
		|*                            CONSTRUCTORS                           *|
		\* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */

		public Mosquito()
		{
			Name = NAME;
			Tier = 1;
			Damage = 2;
			Health = 3;
			Ability = Ability.StartOfBattle;
		}

		public override Character Clone()
		{
			return new Mosquito
			{
				Ability = Ability,
				BattlefieldPosition = BattlefieldPosition,
				Damage = Damage,
				Health = Health,
				Level = Level,
				Name = Name,
				Tier = Tier,
			};
		}

		/// <summary>
		/// Deals 1 Damage at start of Battle the a random oponnent
		/// </summary>
		/// <returns></returns>
		public override AbilityEventArgs TriggerAbility()
		{
			// Randomize target position
			Random rnd = new Random();
			int target = rnd.Next(0, 4);

			return new StartOfBattleEventArgs
			{
				Side = Side.Opponent,
				TargetPosition = target,
				HealthReduced = Level,
				HealthGiven = 0,
			}; // It's to the combat to see if it's a valid position
		}

		/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *\
		|*                           PUBLIC METHODS                          *|
		\* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */


