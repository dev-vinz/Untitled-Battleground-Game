		public static Character GetNewCharacter(int turn)
		{
			Random rnd = new Random();

			int tier = turn < ALL_CHARACTERS.Length ? turn : rnd.Next(ALL_CHARACTERS.Length);
			int nbCharacters = ALL_CHARACTERS[tier].Length;

			int character = rnd.Next(nbCharacters);

			return ALL_CHARACTERS[tier][character].Clone();
		}

